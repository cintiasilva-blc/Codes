'''Escreva um algoritmo que calcule a área de um triângulo, dado a base e a altura.'''

print('___AREA DO TRIÂNGULO___')
base = float(input('Qual o valor da base do triângulo? '))
altura = float(input('Qual o valor da altura do triângulo? '))

area = (base * altura) / 2
print('A area do triangulo é: ', area)
   
